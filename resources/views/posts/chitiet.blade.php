    function opencomment(articleId) {
        // set articleid vào hidden field articleid của modal
        $('#ArticleId').val(articleId);
        // bật modal
        $('#modal-comment').modal('show');
    }

    function resetComment() {
        $('#txtUserName').val('');
        $('#txtEmail').val('');
        $('#txtPhone').val('');
        $('#txtAddress').val('');
        $('#txtComment').val('');
        $('#modal-comment').modal('hide');
    }

    function saveComment() {
        var articleId = $('#ArticleId').val();
        var txtUserName = $('#txtUserName').val();
        var txtEmail = $('#txtEmail').val();
        var txtPhone = $('#txtPhone').val();
        var txtAddress = $('#txtAddress').val();
        var txtComment = $('#txtComment').val();

        var contactModel = {
            ObjectId: articleId,
            Name: txtUserName,
            Phone: txtPhone,
            Email: txtEmail,
            Address: txtAddress,
            Contents: txtComment
        }

        $.ajax({
            url: '/Contact/SaveComment',
            data: contactModel,
            type: "Post",
            dataType: "json",
            timeout: 60 * 1000,
            success: function(result) {
                if (result.success == true) {
                    alert("Gửi thành công")
                    $('#modal-comment').modal('hide');
                } else {
                    alert("Có lỗi xảy ra");
                }
            },
            error: function(x, y, z) {
                if (y != "abort")
                    alert("Có lỗi xảy ra");
            }
        })
    }
