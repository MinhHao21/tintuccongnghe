                                        <!-- <p style="text-align: justify;">Thế giới vừa trải qua những thời khắc đau thương của dịch bệnh, thảm họa. Điều đó khiến cụm từ “hòa bình” càng trở nên trân quý hơn. Để góp một tiếng nói nhỏ bé vào niềm mong muốn chung về một tương lai tươi đẹp, về hòa bình và hạnh phúc của người dân trên toàn thế giới, Nhà hát Nhạc Vũ Kịch Việt Nam tiên phong đưa chương trình Đại hợp xướng <em>Vì hòa bình - A Mass for Peace</em> về Việt Nam để ra mắt khán giả. Chương trình có sự góp mặt của 200 nghệ sĩ, diễn viên đến từ Nhà hát Nhạc Vũ Kịch Việt Nam và các dàn hợp xướng quốc tế khác.</p>

