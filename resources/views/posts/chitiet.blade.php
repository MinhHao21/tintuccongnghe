                                            <strong>
                                                Ng&#224;y 12 v&#224; 13-11-2022, Nh&#224; h&#225;t Nhạc Vũ Kịch Việt Nam sẽ ra mắt chương tr&#236;nh Đại hợp xướng V&#236; h&#242;a b&#236;nh - A Mass for Peace tại Nh&#224; h&#225;t Lớn H&#224; Nội.
