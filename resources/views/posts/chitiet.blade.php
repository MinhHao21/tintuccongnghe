                                        <p style="text-align: center;"><em>Một buổi tập của các nghệ sĩ</em></p>

                                        <p style="text-align: justify;"><em>Vì hòa bình - A Mass for Peace</em> là một tác phẩm của Karl Jenkins, nhà soạn nhạc đến từ xứ Wales. Tác phẩm được ra mắt vào năm 2000 và đã được biểu diễn hàng ngàn lần bởi các dàn nhạc và dàn hợp xướng lớn nhỏ trên khắp thế giới. Tuy nhiên, chưa bao giờ chương trình Đại hợp xướng này đến với đất nước Việt Nam. Vì vậy, Đại hợp xướng <em>Vì hòa bình</em> do Nhà hát Nhạc Vũ Kịch Việt Nam thực hiện ra mắt công chúng có thể coi là bước đột phá mới trong việc từng bước đưa khán giả trong nước tiệm cận với tinh hoa nghệ thuật hàn lâm thế giới.</p>

                                        <p style="text-align: justify;">13 chương nhạc của tác phẩm với những chất liệu âm nhạc phong phú đã vẽ lên một bức tranh nhiều màu sắc. Tác phẩm được mở đầu với âm thanh của tiếng trống diễu hành trang nghiêm. Tiếp đó là những mảng màu của sự hy sinh, mất mát của các nạn nhân trong cuộc thảm họa với những giai điệu trầm buồn và bi thương. Tác phẩm được kết thúc với âm hưởng tươi sáng của sự khởi đầu mới và khát vọng hòa bình cho người dân trên toàn thế giới.</p>

                                        <p style="text-align: justify;">Chương trình có sự tham gia của nhạc trưởng Đồng Quang Vinh, và các nghệ sĩ solist của nhà hát như: ca sĩ Nguyễn Huy Đức, Trịnh Thanh Bình, Đào Tố Loan, Ngô Hương Diệp.</p>

                                        <p style="text-align: justify;"><img alt="" src="/datasite///201807/BAI_VIET_15193/NHI08607%20copy.jpg" style="width: 90%; height: 90%;" /></p>

                                        <p style="text-align: center;"><em>Các nghệ sĩ tập luyện chuẩn bị cho buổi ra mắt</em></p>

                                        <p style="text-align: justify;">Với thế mạnh về dàn nhạc giao hưởng và hợp xướng, Nhà hát Nhạc Vũ Kịch Việt Nam đã có một quyết định đột phá khi kết hợp giữa dàn nhạc, các nghệ sĩ Opera hàng đầu Việt Nam cùng với dàn hợp xướng khổng lồ để tạo nên một bản đại hợp xướng vô cùng hoành tráng và hào hùng.</p>

                                        <p style="text-align: justify;">Cùng với các nghệ sĩ Opera của Nhà hát Nhạc Vũ Kịch Việt Nam, lần đầu tiên, một sự kiện hòa nhạc hợp xướng có thêm sự góp mặt của 4 dàn hợp xướng quốc tế khác tại Việt Nam là Dàn hợp xướng quốc tế Hà Nội Voices, Trường quốc tế Hà Nội Concordia, Hợp xướng thanh thiếu niên V-Art và Trường Erato Hà Nội.</p>

                                        <p style="text-align: justify;">Nói về sự kiện này, ông Phan Mạnh Đức, Giám đốc Nhà hát Nhạc Vũ Kịch Việt Nam, cho biết: Đại hợp xướng<em> Vì hòa bình - A mass for peace</em> là một sự kiện nghệ thuật lớn, ngợi ca hòa bình và khát vọng được sống trong hạnh phúc của người dân trên thế giới. Vì vậy, dù gặp nhiều khó khăn trong việc đưa chương trình này lên sân khấu như nhân lực, chuyên môn, trang thiết bị, thời gian luyện tập, ngôn ngữ… nhưng chúng tôi vẫn quyết tâm, nỗ lực để tác phẩm được vang lên hùng tráng, như một thông điệp chung của chúng ta về một tương lai tươi sáng và khán giả Việt Nam có thêm cơ hội thưởng thức tinh hoa của nghệ thuật hàn lâm thế giới”.</p>

                                        <p style="text-align: justify;">Hy vọng, tác phẩm lần này sẽ để lại những ấn tượng tốt đẹp và nhiều cảm xúc với những khán giả yêu nghệ thuật.</p>

